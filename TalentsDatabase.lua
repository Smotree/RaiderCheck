-- Получить данные таланта из базы
function RaiderCheck_GetTalentFromDB(className, tabIndex, talentIndex)
	if not RaiderCheck_TalentsDB[className] then
		return nil
	end
	if not RaiderCheck_TalentsDB[className][tabIndex] then
		return nil
	end
	return RaiderCheck_TalentsDB[className][tabIndex][talentIndex]
end

-- Получить количество талантов в дереве
function RaiderCheck_GetNumTalentsInTree(className, tabIndex)
	if not RaiderCheck_TalentsDB[className] then
		return 0
	end
	if not RaiderCheck_TalentsDB[className][tabIndex] then
		return 0
	end

	local count = 0
	for _ in pairs(RaiderCheck_TalentsDB[className][tabIndex]) do
		count = count + 1
	end
	return count
end

